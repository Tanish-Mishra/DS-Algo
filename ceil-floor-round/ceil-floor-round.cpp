#include <iostream>
#include <cmath>

using namespace std;

int main() {

	// Basically ceil-floor-round is used for floating point numbers

	float a,b,c;

	cin>>a>>b>>c;

	cout << "The ceil value of a is " << ceil(a) << " The Floor value of the b is " << floor(b) << " The round value of c is " << round(c) << endl;

	return 0;
}